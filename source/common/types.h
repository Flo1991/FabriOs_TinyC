/** 8 bit usigned type definition, is unsigned char */
typedef unsigned char u8;

/** 8 bit signed type definition, is (signed) char */
typedef signed char i8;

/** 16 bit usigned type definition; unsigned int for avr gcc is 16 bit */
typedef unsigned int u16;

/** 16 bit signed type definition; signed int for avr gcc is 16 bit */
typedef signed int i16;

/** 32 bit usigned type definition; unsigned long for avr gcc is 32 bit */
typedef unsigned long u32;

/** 32 bit signed type definition; signed long for avr gcc is 32 bit */
typedef signed long i32;

/** define type bool as usigned char, so 8 bit */
typedef unsigned char bool;

/** define bool value true as 1 */
#define true 1

/** define bool value false as 0 */
#define false 0

